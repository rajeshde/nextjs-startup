import { useState, useContext } from "react";
import Head from "next/head";
import { useRouter } from "next/router";
import styled from "styled-components";

import { AppContext } from "app/context";
import styles from "styles/Home.module.css";

const DetailInput = styled.textarea`
  border: 1px solid #ccc;
  border-radius: 5px;
  width: 400px;
  height: 300px;
  padding: 10px;
  font-size: 15px;
  color: #666;
  &:focus {
    outline: none;
    box-shadow: none;
  }
`;

const StyledSection = styled.div`
  width: 400px;
  display: flex;
  justify-content: space-between;
  align-items: center;
`;
const SaveButton = styled.button`
  width: 60px;
  height: 25px;
  padding: 5px 10px;
  border-radius: 15px;
  background-color: #136833;
  color: #fff;
  border: none;
  cursor: pointer;
  ${({ disabled }) =>
    disabled &&
    `
    background-color: #eee;
    color: #222;
    cursor: not-allowed;
  `}
`;

const Details = () => {
  const {
    query: { id },
  } = useRouter();
  const { details, handleOnUpdateDetail } = useContext(AppContext);
  const [note, setNote] = useState(details[id] || "");

  const handleOnSave = () => handleOnUpdateDetail(id, note);

  return (
    <div className={styles.container}>
      <Head>
        <title>Todo Detail</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h2>{id}</h2>
        <StyledSection>
          <p>Note</p>
          <SaveButton
            onClick={handleOnSave}
            disabled={note === details[id]}
            type="button"
          >
            {note === details[id] ? "Saved" : "Save"}
          </SaveButton>
        </StyledSection>
        <DetailInput value={note} onChange={(e) => setNote(e.target.value)} />
      </main>
    </div>
  );
};

export default Details;
